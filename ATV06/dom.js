//--Atividade 06 - Soma os numeros de 1 a 50 de forma decrescente--//
let contador = Number(prompt("Digite o numero entre 1 e 50:"))
let soma = 0
while(contador > 0){      
    soma = soma + contador
    contador--    
}
console.log(`Soma = ${soma}`)
